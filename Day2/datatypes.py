# integer

print('integer')

print('decimal ', 10**10)
print(type(10**10))

print('binary -> decimal', 0b111)
print(type(0b111))

print('octal -> decimal', 0o10)
print(type(0o10))

print('hexadecimal -> decimal', 0xb)
print(type(0xb))

# float

print('float')

print('float', 3.1415)
print(type(3.1415))

print('plancks constant', 6.62e-34)
print(type(6.62e-34))

# string

print("string")
print(type("string"))

print("", type(""))

print("I\nlove\npython")
print(type("I\nlove\npython"))

print('''hello python's world''')

# boolean

print(True, type(True))
print(False, type(False))

# complex

print(1 + 2j)
print(type(1 + 2j))

# conversion
print('decimal -> hex', format(10, 'x'))

print('deciaml -> binary', format(7, 'b'))

print('deciaml -> binary', format(8, 'o'))

